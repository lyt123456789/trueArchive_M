package cn.com.trueway.mobileInterface.spacial.bean;

public class SpecialFj {
	private String fj_name = "";	//附件名称
	private String fj_instr = "";	//附件说明
	private String fj_id = "";		//附件id
	private String fj_time = "";	//附件时间
	public String getFj_name() {
		return fj_name;
	}
	public void setFj_name(String fj_name) {
		this.fj_name = fj_name;
	}
	public String getFj_instr() {
		return fj_instr;
	}
	public void setFj_instr(String fj_instr) {
		this.fj_instr = fj_instr;
	}
	public String getFj_time() {
		return fj_time;
	}
	public void setFj_time(String fj_time) {
		this.fj_time = fj_time;
	}
	public String getFj_id() {
		return fj_id;
	}
	public void setFj_id(String fj_id) {
		this.fj_id = fj_id;
	}
	
}
